package exercicios;

/** Exercicio "Na Pratica 01". */
//1: Imprima na tela todos os números de 150 a 300.
public class Napratica01 {
  /** Executa o programa.*/
  public static void main(String[] args) {
    //laço de impressão condicionado ao range solicitado
    for (int cont = 150; cont <= 300; cont++) {
      System.out.println(cont);
    }
  }
}